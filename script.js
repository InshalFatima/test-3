function add() {
    let txtNumber = document.getElementById("txtNumber");
    let newNumber = parseInt(txtNumber.value) + 1;
    txtNumber.value = newNumber;
}

function subtract() {
    let txtNumber = document.getElementById("txtNumber");
    let newNumber = parseInt(txtNumber.value) - 1;
    txtNumber.value = newNumber;
}





